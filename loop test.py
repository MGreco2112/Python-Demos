# loop tutorial

#basic loop
for x in range(10):
    print(x)

#break line
print()

#index of string utilizing range for condition
word = "abcdefghij"

for x in range(10):
    print(word[x])

#break line
print()

#for in loop

for x in word:
    print(x)

#break line
print()

